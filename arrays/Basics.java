package arrays;

public class Basics {

    /*
    * ---------What is a static Array?-----------
    * - A static array is a fixed length container containing n
    * elements indexable from the range [0,n-1] where n is the length
    * an array.
    *
    * ---------What is  meant by Indexable?-----------
    * - This means that each slot/index in the array can be referenced
    * with a number.
    *
    * ---------Complexity-----------
    *             (Static) (Dynamic)
    * - Access      O(1)    O(1)
    * - Search      O(n)    O(n)
    * - Insertion   NA      O(n)
    * - Appending   NA      O(1)
    * - Deletion    NA      O(n)
    *
    *
    *
    * */
}
