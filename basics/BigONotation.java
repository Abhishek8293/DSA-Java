package basics;

public class BigONotation {

    /*
    *As programmers, we often find ourselves asking the same two questions over and over again:-
    * - How much time does this algorithm need to finish?
    * - How much space does this algorithm need for its computation?
    *
    * ---------------Big-O Notation-------------
    * - Big-O notation gives an upper bound of the complexity in the "Worst",
    *   case, helping to quantify performance as the input size becomes
    *   arbitrary large.
    *
    *   - n is the size of the input
    *   - Complexities ordered in from smallest to largest
    *   - Constant Time: O(1)
    *   - Logarithmic Time: O(log(n))
    *   - Linear Time: O(n)
    *   - Linearithmic Time: O(n*log(n))
    *   - Quadratic Time: O(n2)
    *   - Cubic Time: O(n3)
    *   - Exponential Time: O(b**n)
    *   - Factorial Time: O(n!)
    *
    * ---------------Big-O Properties-------------
    * - O(n+c) = O(n)
    * - O(c*n) = O(n), C > 0
    * - Finding all subsets of a set = O(2**n)
    * - Finding all permutations of a string = O(n!)
    * - Sorting using merge sort = O(n*log(n))
    * - Iterating over all the cells in a matrix of size n by m= O(n*m)
    *
    *
    * */

}
